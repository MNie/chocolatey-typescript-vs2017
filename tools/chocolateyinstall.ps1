$packageArgs = @{
  packageName   = $packageName
  unzipLocation = $toolsDir
  fileType      = 'exe'
  url           = $url

  silentArgs   = "/quiet /norestart /log `"$env:TEMP\chocolatey\$($packageName)\$($packageName).Install.log`""
  validExitCodes= @(0)

  softwareName  = 'TypeScript Tools for Microsoft Visual Studio 2017 2.6.2'
  checksum		= '907a70d26a7c432cbdff547cae50b9ce678b190ae4a975753ac307180ba2138c'
  checksumType  = 'sha256'
}

Install-ChocolateyPackage @packageArgs